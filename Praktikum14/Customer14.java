package Praktikum14;

public class Customer14 {
    public int id;
    public String name;

    public Customer14() {

    }

    public Customer14(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String toString() {
        return "ID: " + this.id + " Nama: " + this.name;
    }
}